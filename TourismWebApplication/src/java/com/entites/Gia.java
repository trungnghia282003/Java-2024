public class Gia {

    private int id;
    private String mucGia;

    public Gia() {
    }

    public Gia(int id, String mucGia) {
        this.id = id;
        this.mucGia = mucGia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMucGia() {
        return mucGia;
    }

    public void setMucGia(String mucGia) {
        this.mucGia = mucGia;
    }

}
